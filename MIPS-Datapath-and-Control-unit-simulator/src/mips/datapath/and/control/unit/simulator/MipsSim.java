    private void InstRItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_InstRItemStateChanged
       if(InstR.getSelectedItem()=="sll")
        {
            shamt.setEnabled(true);
            rrs.setEnabled(false);
            rrt.setEnabled(true);
            rrd.setEnabled(true);
            rrs.setSelectedItem("$0");
        }
        else if(InstR.getSelectedItem().equals("jr"))
        {
            shamt.setEnabled(false);
            rrs.setEnabled(true);
            rrt.setSelectedItem("$0");
            rrt.setEnabled(false);
            rrd.setSelectedItem("$0");
            rrd.setEnabled(false);
        } else {
            shamt.setText("0");
            shamt.setEnabled(false);
            rrs.setEnabled(true);
            rrt.setEnabled(true);
            rrd.setEnabled(true);
        }
    }//GEN-LAST:event_InstRItemStateChanged

