    private void addinstActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addinstActionPerformed
        if(RFormat.isSelected())
        {
         Memory.addInstruction((new RFormat(rlabel.getText(),pcInitial,InstR.getSelectedItem().toString(),rrs.getSelectedItem().toString(),
                 rrt.getSelectedItem().toString(),rrd.getSelectedItem().toString(),Integer.parseInt(shamt.getText()))));
        pcInitial+=4;
        }
        else if(IFormat.isSelected())
        {
            if(!(InstI.getSelectedItem().equals("beq")))
            {
         Memory.addInstruction(new IFormat(ilabel.getText(),pcInitial,InstI.getSelectedItem().toString(),irs.getSelectedItem().toString(),
                 irt.getSelectedItem().toString(),Integer.parseInt(iconstant.getText())));
            }
            else
                Memory.addInstruction(new IFormat(ilabel.getText(),pcInitial,InstI.getSelectedItem().toString(),
                        irs.getSelectedItem().toString(),irt.getSelectedItem().toString(),iconstant.getText()));
        pcInitial+=4;
        }
        else if(JFormat.isSelected())
        {
            Memory.addInstruction(new JFormat(jlabel.getText(),pcInitial,InstJ.getSelectedItem().toString(),jaddress.getText()));
            pcInitial+=4;
        }
    }//GEN-LAST:event_addinstActionPerformed

